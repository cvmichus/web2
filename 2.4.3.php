
<p>
El  programa  metropolitano  deberá  contener  un  diagnóstico integral  de  la  zona  conurbada  o  zona  metropolitana,  una  visión prospectiva de largo plazo, la definición de los objetivos, metas, criterios, políticas y estrategias; así como los proyectos estratégicos y acciones prioritarias.
</p>
<p>
Articular  los  distintos  instrumentos  de  planeación  de  los  tres  órdenes  de gobierno;
</p>
<p>
Establecer los elementos de la estructura urbana y de la clasificación básica del territorio en zonas urbanas, urbanizables y no urbanizables, así como las áreas de suelo estratégico a que se sujetarán los programas municipales que se ubiquen en dicha zona conurbada o zona metropolitana;
</p>
<p>
Definir las acciones de movilidad urbana integrada y sustentable, privilegiando el uso del transporte público masivo, de sistemas no motorizados y de aquéllos de menor impacto ambiental;
</p>
<p>
Definir  las  políticas  e  instrumentos  para  el  ordenamiento,  reestructuración, localización,   mejoramiento   y   crecimiento   de   la   infraestructura   y los equipamientos  en  la  zona  conurbada  o  zona  metropolitana  y  su  área  de influencia,  así  como  los  polígonos  de  protección  y  amortiguamiento  de instalaciones riesgosas o de seguridad nacional;
</p>
<p>
Determinación de los proyectos estratégicos y acciones prioritarias.
</p>

