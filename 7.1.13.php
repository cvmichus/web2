    <td style="text-align: justify;">
Dependencia del Poder Ejecutivo Federal, a cargo del despacho de los siguientes asuntos:<br>
 I.- Estudiar y formular los proyectos de leyes y disposiciones impositivas, y las leyes de ingresos federal y del Departamento del Distrito Federal
<br>II.- Cobrar los impuestos, derechos, productos y aprovechamientos federales en los términos de las leyes;
<br>III.- Cobrar los derechos, impuestos, productos y aprovechamientos del Distrito Federal, en los términos de la Ley de Ingresos del Departamento del Distrito Federal y las leyes fiscales correspondientes;<br>
IV.- Determinar los criterios y montos globales de los estímulos fiscales; estudiar y proyectar sus efectos en los ingresos de la Federación, y, con la excepción de lo previsto en la fracción VI del Artículo 34, resolver en los casos concretos su aplicación, así como vigilar y evaluar sus resultados. Para ello escuchará a las dependencias responsables de los sectores correspondientes;<br>
V.- Dirigir los servicios aduanales y de inspección y la policía fiscal de la Federación,
VI.- Proyectar y calcular los ingresos tanto de la Federación, como del Departamento del Distrito Federal;<br>
VII.- Mantener al corriente el evalúo de los bienes muebles nacionales, y reunir, revisar y determinar las normas y procedimientos para realizarlo; así como compilar, revisar y determinar las normas y procedimientos para los inventarios de bienes muebles que deban llevar otras dependencias;
<br>VIII.- Practicar inspecciones y reconocimientos de existencias en almacenes, con objeto de asegurar el cumplimiento de las disposiciones fiscales;
<br>IX.- Intervenir en todas las operaciones en que se haga uso del Crédito Público;
<br>X.- Manejar la deuda pública de la Federación y del Departamento del Distrito Federal;
<br>XI.- Dirigir la política monetaria y crediticia.
<br>XII.- Administrar las casas de moneda y ensaye;
<br>XIII.- Ejercer las atribuciones que le señalan las leyes de instituciones de crédito, seguros y fianzas;
<br>XIV.- Intervenir en la representación del interés de la Federación y del Departamento del Distrito Federal en controversias fiscales, y
<br>XV.- Los demás que le atribuyan expresamente las leyes y reglamentos.


