
<p style="color:#403c3b;font-size:10pt;font-weight:bold;">
Características
</p>

<p style="text-align: justify;">
Los Programas Municipales de Desarrollo Urbano tienen por objeto el desarrollo urbano en el territorio municipal. Estos programas contienen la zonificación y las líneas de acción específicas para la ordenación y regulación de los centros de población del municipio.
</p>

<p style="color:#403c3b;font-size:10pt;font-weight:bold;">
Ámbito de actuación Municipal
</p>

<p style="color:#403c3b;font-size:10pt;font-weight:bold;">
Instancia de Formulación y Aprobación 
</p>

<p style="text-align: justify;">
<span style="color:#403c3b;font-size:10pt;font-weight:bold;">CHIAPAS:</span> Tendrán que ser elaborados por el Instituto Ciudadano de Planeación Municipal o el Área competente del Ayuntamiento, quien consultará a la Comisión Consultiva de Desarrollo Urbano; que a su vez opinará y dictaminará. Serán finalmente aprobados por el Municipio.
</p>
<p style="text-align: justify;">
<span style="color:#403c3b;font-size:10pt;font-weight:bold;">MICHOACÁN:</span> formulados, aprobados, ejecutados, controlados y evaluados por los ayuntamientos correspondientes, a través de la coordinación que para el efecto establezcan los gobiernos municipales con el Gobierno del Estado. 
</p>
<p style="text-align: justify;">
<span style="color:#403c3b;font-size:10pt;font-weight:bold;">GUERRERO:</span> Autoridades municipales formulan y aprueban.
