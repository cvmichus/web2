<p>
IMPLAN
<br> 
De acuerdo con la AMIMP, los institutos municipales de planeación (IMPLAN) son organismos técnicos, consultivos y de participación social en materia de planeación para el desarrollo municipal. 
<br>
Son la institución responsable de conducir los procesos de planeación para el desarrollo municipal.
<br>
Promueven la participación activa y corresponsable de la sociedad y el desarrollo de una cultura urbana. 
Son un instrumento de la estructura municipal que amplía sus capacidades de gestión.
<br>
 Son un centro de reflexión e investigación que realiza estudios y prepara proyectos en temas esencialmente municipales. Articulan los esfuerzos de los diferentes actores del municipio (públicos, privados y sociales) potenciando capacidades, competencias y recursos.
