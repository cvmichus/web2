¿Qué dice al respecto la Constitución Federal?<br>

La Constitución Política de los Estados Unidos Mexicanos, como ley suprema de la Unión, manifiesta en su art. 27 párrafo tercero, que la regulación debe “lograr… el mejoramiento de las condiciones de vida de la población rural y urbana”. 
Para ello, en su art. 73 fracc XXIX-C y art. 115 y 122. Faculta al Congreso de la Unión y a los municipios para expedir leyes que establezcan la concurrencia del gobierno federal de los estados y de los municipios en materia de asentamientos humanos 
En la distribución de competencias, le asigna al municipio las facultades para la expedición de bandos de policía y buen gobierno, reglamentos, circulares y disposiciones administrativas de observancia general dentro de sus respectivas jurisdicciones, que organicen la administración pública municipal, regulen las materias, procedimientos, funciones y servicios públicos de su competencia creando el Sistema Nacional de Planeación para el Desarrollo.


