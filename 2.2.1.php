<p>
Si bien es cierto que cada estado establece a detalle en sus leyes específicas de desarrollo urbano y ordenamiento territorial el contenido de los Programas de Desarrollo Urbano,  en general estos documentos contienen los siguientes elementos básicos que
hacen posible su congruencia y uniformidad metodológica para su debida ejecución técnica, jurídica y administrativa que darán forma y espacialidad a los proyectos y objetivos de las Zonas Económicas Especiales. 

</p>
<p>
Introducción: La denominación, antecedentes y ámbito espacial de aplicación;<br><br>

Diagnóstico: en el que se analizarán la situación actual y las tendencias del área o sector del desarrollo urbano que comprenda el programa en sus aspectos socioeconómicos, físicos, poblacionales, de infraestructura, equipamiento, servicios, vivienda y demás componentes urbanos;<br><br>

Objetivos: en los que se contemplarán, los propósitos o finalidades que se pretenden alcanzar con la ejecución del programa respectivo;<br><br>

Estrategias: en las que se establecerán los lineamientos y políticas del programa y las alternativas para la ejecución del mismo;<br><br>

Metas: en las que se precisarán las acciones, obras o servicios, de acuerdo con los objetivos planteados en el corto, mediano y largo plazo;<br><br>

Acciones de inversión: en las que se contendrán las prioridades del gasto público y privado;<br><br>

Bases Financiero-Programáticas: en las que se preverán los recursos disponibles paraalcanzar los lineamientos programáticos;<br><br>

Proyectos estratégicos: contendrán los proyectos que deberán ejecutarse de manera prioritaria;<br><br>

Corresponsabilidad sectorial: determinará las acciones, obras e inversiones concretas que deban realizarse y establecerá los responsables de su jecución;<br><br>

Criterios de concertación con los sectores público, social y privado;<br><br>
Instrumentos de política: contendrán el conjunto de medidas, mecanismos y disposiciones jurídicas, técnicas y administrativas, que permitan la institucionalización, ejecución, control y evaluación del programa;<br><br>

Estructura de organización y coordinación: para la elaboración, ejecución, control, evaluación, actualización y modificación del programa, y<br><br>

Anexo gráfico que expresa en una forma descriptiva y clara el contenido básico del programa y la ubicación espacial de los proyectos y acciones.<br><br>


</p>

