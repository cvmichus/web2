
<p>

Función:<br><br> Mecanismo de coordinación institucional y de concertación de acciones e inversiones con los sectores social y privado.
Formular y aprobar el programa de ordenación de la Zona Metropolitana o conurbada interestatal e intermunicipal, gestionar y evaluar su cumplimiento.
<br><br>
Integración:<br><br> por un representante de cada entidad federativa y de cada municipio que lo integre, así como un representante de la Secretaría quien lo presidirá. Podrá contar con subcomisiones o consejos integrados por igual número de representantes de los tres órdenes de gobierno.
<br><br>
Funcionamiento: <br><br>
En las zonas metropolitanas interestatales y conurbaciones interestatales se constituirá una comisión de ordenamiento, que tendrá carácter permanente. Sesionarán por lo menos trimestralmente. 



