<br>
 Son encabezados por un Consejo con participación mayoritaria de ciudadanos. Constituyen un espacio destinado al pensamiento estratégico permanente para lograr políticas públicas con visión de largo plazo.
<br>
Trasciende al periodo de gobierno municipal y da continuidad a los proyectos y las acciones con visión de futuro.

 </p>
 
 <p>
Estatus jurídico recomendado para un  Instituto
<br> 
Uno de los fundamentos en los que se basa el concepto de Instituto de Planeación es la continuidad
de sus acciones, por ello, se ha recomendado que éstos adquieran la figura de organismo público
descentralizado de la administración municipal.
 
 </p>
 
 <p>
Funciones clave
 
1. Instrumento técnico; normas técnicas; sustento técnico; estructura técnico-operativa; apoyo
técnico; apoyo técnico al COPLADEM
2. Establecer continuidad en los procesos de planeación; continuidad en las políticas públicas para
que trascienden a período de las administraciones municipales;
3. Auxiliar a la autoridad municipal; coadyuvar con la autoridad municipal
4. Realización de la Planeación Urbana y ordenamiento territorial; desarrollo urbano, política ambiental y de planeación;
formular y proponer políticas y o proyectos de desarrollo urbano; sistema normativo del
desarrollo urbano municipal
5. Planeación integral; formular, actualizar, revisar y evaluar planes y programas del Plan de
Desarrollo Integral; planeación integral de suelo urbano y reservas territoriales
6. Planeación participativa; participación ciudadana en la elaboración de planes, programas y
proyectos; promover la participación social; participar en la presentación de las consultas
públicas;
7. Campañas de educación para concientizar la población
8. Coordinación y concentración en materia de planeación con los Gobiernos estatales, federales
etc.
 
 </p>
 
 <p>
Atribuciones de los Institutos
 
1. Fungir como órgano de análisis;
2. Establecer la continuidad de los procesos de planeación; que trasciendan a los periodos
municipales para consolidar planes y programas de desarrollo
3. Cada tres años proponer la actualización del Plan Metropolitano de Ordenamiento Territorial y
Desarrollo Urbano
4. Auxiliar a la autoridad municipal en materia de desarrollo urbano y realización de sus funciones;
Asesorar y coadyuvar técnicamente el Ayuntamiento y dependencias del gobierno municipal
5. Elaboración, evaluación y actualización de los diferentes planes de desarrollo; proponer al
Ayuntamiento los programas a seguir a corto, mediano y largo plazo;
6. Ordenar los asentamientos urbanos, Evitando el crecimiento desordenado, verificando los
proyectos de zonificación; estrategias y acciones para la conservación, mejoramiento,
crecimiento y zonificación de los centros de población; establecimiento de adecuadas
provisiones, usos, reservas y destinos de tierras, aguas, playas. Islas, islotes, arrecifes a efectos
de ejecutar obras y planear y regular la conservación, mejoramiento y crecimiento sustentable
de los centros de población; uso eficiente de energía
7. Control eficiente de la contaminación
8. El uso racional y eficiente del agua, y la utilización de la tecnología adecuada para el tratamiento
de aguas residuales
9. Gestión urbanística
10. Promoviendo el uso eficiente del suelo; localizar e inventariar las superficies baldías dentro de
las zonas plenamente urbanizadas y proponer su aprovechamiento; privilegiando la
consolidación de la mancha urbana orientando el crecimiento hacia dentro;

