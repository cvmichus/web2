<p>
¿Por qué un mecanismo de gobernanza?
</p>
<p>
La Ley General de Asentamientos Humanos, Ordenamiento Territorial y Desarrollo Urbano (LGAHOTyDU), en su artículo 4 capítulo IV obliga a la planeación del ordenamiento territorial a ser equilibrada, coherente y acorde con los planes y políticas nacionales, de manera que de conformidad con el artículo 5 todos los instrumentos de planeación y sus respectivos mecanismos de gobernanza deben regirse por los principios de coherencia y racionalidad; sin importar el orden de gobierno del cual emanen. 
</p>
 <p>
En ese tenor es importante que tanto los planes parciales, municipales y metropolitanos de desarrollo urbano; así como los programas de desarrollo de las zonas económicas especiales estén alineados y persigan objetivos comunes. Y que los mecanismos de coordinación, concentración y gobernanza que se generen para su cumplimiento, tengan en cuenta todos los instrumentos que impactan en el desarrollo de la escala territorial a la que se refieran. 
 
Así mismo, la LGAHOTyDU obliga a la SEDATU y otras entidades de la Administración Pública Federal (art. 12) a promover la celebración de  convenios y acuerdos de coordinación entre la Federación y las entidades federativas con la intervención de los municipios, así como de convenios de concertación con los sectores social y privado.  Posteriormente en su artículo 13, señala que también los municipios y las entidades federativas pueden suscribir convenios de coordinación para que los últimos asuman el ejercicio de funciones que en materia de asentamientos humanos y desarrollo urbano le corresponden a los municipios o para que los municipios asuman las funciones o servicios que les corresponden a las entidades federativas. 
</p>
<p>
Definición de mecanismos y sustento legal
<br>
Consejos Locales y Municipales de Ordenamiento Territorial, Desarrollo Urbano y Metropolitano. 
<br>
El artículo 19 de la LGAHOTyDU establece que para asegurar la consulta y la deliberación de las políticas de ordenamiento territorial  y planeación del desarrollo urbano y desarrollo metropolitano, conforme al Sistema de Planeación Democrática (art. 26 CPEUM); las entidades federativas y los municipios de acuerdo con sus respectivas competencias, conformen órganos auxiliares de participación ciudadana y de conformación plural. 
 <br>
1. Consejos estatales de ordenamiento territorial y desarrollo urbano<br> 
2. Las comisiones metropolitanas y de conurbaciones <br>
3. Los consejos municipales de desarrollo urbano y vivienda.<br>
<br>
Dichos consejos deben ser creados por los ejecutivos de las entidades federativas y los municipios y de recibir su apoyo para la operación. 
 <br>
Cada consejo debe contar con un reglamento que defina el número de sus miembros incluyendo al sector social y gubernamental de los órganos de gobierno correspondientes, así como a los colegios de profesionistas, instituciones académicas; órganos empresariales y expertos del sector, entre otros. 

</p>
<p>
Planeación y gobernanza Territorial
<br>
La LGAHOTDU prevé en su Capítulo Quinto, la planeación de las Zonas Metropolitanas y conurbadas.  
<br>
La SEDATU, como cabeza de sector, es la encargada de la emisión de lineamientos a través de los cuales se establecerán los métodos y procedimientos para medir y asegurar que los proyectos y las acciones vinculados con políticas de interés metropolitano cumplan con sus objetivos. <br>
La gestión de las áreas metropolitanas y las conurbaciones se efectuará de manera obligatoria (según lo marca la LGAHOTyDU) a través de:<br>
Comisión de ordenamiento metropolitano o conurbación<br>
Consejo consultivo<br>
Mecanismos de carácter técnico (según la entidad)<br>
Instancias para la prestación de servicios comunes<br>
Mecanismos y fuentes de financiamiento<br>
<br><br>
Recomendación
<br><br>
De acuerdo con el art. 22 de la LGAHOTyDU, la planeación, regulación y evaluación del Ordenamiento Territorial de los Asentamientos Humanos y del Desarrollo Urbano de los Centros de Población; como parte del SNPD como una política de carácter global, sectorial y regional; debe coadyuvar al logro de los objetivos del Plan Nacional de Desarrollo y de los programas federales, estatales y municipales entre los que no se debe dejar de lado el Programa de  Desarrollo de las Zonas Económicas Especiales. 
<br>
Es por ello que en los municipios ubicados en ZEE, se recomienda incorporar representantes de la Zona en sus respectivos órganos auxiliares, para garantizar el seguimiento a los programas de desarrollo de ZEE; y su alineación con los Planes Municipales y Metropolitanos de Ordenamiento Territorial y Desarrollo Urbano. De manera que las ZEE puedan interactuar en la formulación, aplicación y vigilancia de las políticas de ordenamiento territorial y planeación del desarrollo urbano y metropolitano. 
</p>

