
<p>
<strong>Consejo Nacional de Ordenamiento Territorial y Desarrollo Urbano</strong>
<br><br>
Función:<br><br>
 instancia de carácter consultivo, de conformación plural y de participación ciudadana, convocada por el titular de la SEDATU, para la consulta, asesoría, seguimiento y evaluación de las políticas nacionales en la materia.
<br><br>
Integración: estará integrado por cuarenta y dos miembros de acuerdo con la siguiente distribución:
<br><br>
I.     El Titular de la Secretaría de Desarrollo Agrario Territorial y Urbano, quien lo presidirá;<br>
II.     Un Secretario Técnico, designado por el Presidente del Consejo;<br>
III.    Los titulares de las siguientes Secretarías, Organismos Descentralizados y Órganos Desconcentrados de la Administración Pública Federal:<br><br>
a)    Comisión Nacional de Vivienda.<br>
b)    Instituto Nacional del Suelo Sustentable.<br>
c)    Registro Agrario Nacional.<br>
d)    Secretaría de Agricultura, Ganadería, Desarrollo Rural, Pesca y Alimentación.<br>
e)    Secretaría de Comunicaciones y Transportes.<br>
f)     Secretaría de Economía.<br>
g)    Secretaría de Energía.<br>
h)    Secretaría de Gobernación.<br>
i)     Secretaría de Hacienda y Crédito Público.<br>
j)     Secretaría de Medio Ambiente y Recursos Naturales.<br>
<br><br>
IV.   El Presidente del Instituto Nacional de Estadística y Geografía;<br><br>
V.    Dos representantes de la Cámara de Diputados del Congreso de la Unión;<br>
VI.   Dos representantes del Senado de la República;<br>
VII.   Cuatro representantes de los gobiernos de las entidades federativas;<br>
VIII.  Cuatro representantes de gobiernos municipales;<br>
IX.   Cuatro representantes pertenecientes a instituciones académicas;<br>
X.    Cuatro representantes de organismos gremiales;<br>
XI.   Cuatro representantes de organismos del sector privado, y<br>
XII.   Cinco representantes de organismos de la sociedad civil.<br>
<br><br>
Funcionamiento: <br><br>  Los acuerdos se tomarán por mayoría simple de los presentes y será necesaria la presencia de al menos la mitad más uno de sus integrantes para que tengan validez.
<br><br>
Los integrantes del Consejo tendrán voz y voto con excepción del Secretario Técnico. En caso de empate el Presidente del Consejo tendrá voto de calidad.
Los integrantes del Consejo desempeñarán sus funciones de manera honoraria, podrán designar representantes que ocupen, al menos, el nivel de Subsecretario; los de las entidades federativas deberán tener nivel de Secretario y preferentemente ser responsables de la cartera de desarrollo territorial y urbano.
<br><br>
Los integrantes durarán dos años en su encargo, los pertenecientes a instituciones académicas, organismos gremiales, organismos del sector privado y organismos de la sociedad podrán ser ratificados por un periodo igual.


