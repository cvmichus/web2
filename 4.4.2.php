Consejos Locales y Municipales de Ordenamiento Territorial, Desarrollo Urbano y Metropolitano. 
<br><br>
El artículo 19 de la LGAHOTyDU establece que para asegurar la consulta y la deliberación de las políticas de ordenamiento territorial  y planeación del desarrollo urbano y desarrollo metropolitano, conforme al Sistema de Planeación Democrática (art. 26 CPEUM); las entidades federativas y los municipios de acuerdo con sus respectivas competencias, conformen órganos auxiliares de participación ciudadana y de conformación plural. 
 <br><br>
1. Consejos estatales de ordenamiento territorial y desarrollo urbano <br><br>
2. Las comisiones metropolitanas y de conurbaciones <br><br>
3. Los consejos municipales de desarrollo urbano y vivienda.<br><br>

Dichos consejos deben ser creados por los ejecutivos de las entidades federativas y los municipios y de recibir su apoyo para la operación. 
 <br><br>
Cada consejo debe contar con un reglamento que defina el número de sus miembros incluyendo al sector social y gubernamental de los órganos de gobierno correspondientes, así como a los colegios de profesionistas, instituciones académicas; órganos empresariales y expertos del sector, entre otros. 




