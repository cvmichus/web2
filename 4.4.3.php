
<p>

La LGAHOTDU prevé en su Capítulo Quinto, la planeación de las Zonas Metropolitanas y conurbadas.  
<br><br>
La SEDATU, como cabeza de sector, es la encargada de la emisión de lineamientos a través de los cuales se establecerán los métodos y procedimientos para medir y asegurar que los proyectos y las acciones vinculados con políticas de interés metropolitano cumplan con sus objetivos. 
<br><br>
La gestión de las áreas metropolitanas y las conurbaciones se efectuará de manera obligatoria (según lo marca la LGAHOTyDU) a través de:<br><br>
Comisión de ordenamiento metropolitano o conurbación<br><br>
Consejo consultivo<br><br>
Mecanismos de carácter técnico (según la entidad)<br><br>
Instancias para la prestación de servicios comunes<br><br>
Mecanismos y fuentes de financiamiento<br><br>
