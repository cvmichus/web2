<p>
Los Programas Parciales de Desarrollo Urbano son instrumentos técnico-jurídicos y administrativos de la planeación urbana, para la inducción y precisión del ordenamiento y regulación, en la consolidación de un área específica estratégicamente determinada dentro de un centro de población, sus usos y destinos, así como la determinación de las acciones de conservación, mejoramiento y crecimiento requeridas y contendrán además de los elementos básicos.
</p>
<p>

Localizar, diseñar, estructurar, mejorar y ejecutar las obras de infraestructura pública y equipamiento urbano, mediante la identificación de:<br><br>
Las características de las vialidades urbanas, señalando los alineamientos y derechos que deberán respetarse; y las características de los sistemas de transporte de pasajeros y de carga que se utilicen en las vialidades urbanas;<br><br>
Fomentar la movilidad urbana integrada y sustentable, privilegiando el uso del transporte público masivo, de sistemas no motorizados y de aquéllos de menor impacto ambiental<br><br>
Identificar y clasificar el patrimonio cultural urbano y arquitectónico y las áreas de valor escénico, y establecer, para su protección, las modalidades y restricciones al uso del suelo y a las construcciones;<br><br>
Identificar las zonas de riesgo y establecer las modalidades y restricciones al uso del suelo y a las construcciones<br><br>
Identificar los instrumentos financieros que permitan la suficiencia de recursos <br><br>
Determinar los   usos   y   destinos   predominantes   y   compatibles,   condicionados   e incompatibles<br><br>

