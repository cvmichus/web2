
<p>
●	El Programa Municipal de Desarrollo Urbano tiene como objetivo localizar, diseñar, estructurar, mejorar y ejecutar las obras de infraestructura pública y equipamiento urbano, mediante la identificación de las características de las vialidades urbanas, señalando los alineamientos y derechos que deberán respetarse; y las características de los sistemas de transporte de pasajeros y de carga que se utilicen en las vialidades urbanas.<br><br>
●	Fomenta la movilidad urbana integrada y sustentable, privilegiando el uso del transporte público masivo, de sistemas no motorizados y de aquéllos de menor impacto ambiental.<br><br>
●	Identifica y clasifica el patrimonio cultural urbano y arquitectónico y las áreas de valor escénico, y establece, para su protección, las modalidades y restricciones al uso del suelo y a las construcciones.<br><br>
●	Identifica las zonas de riesgo y establece las modalidades y restricciones al uso del suelo y a las construcciones.<br><br>
●	Identifica los instrumentos financieros que permitan la suficiencia de recursos.
●	Determina los   usos   y   destinos   predominantes   y   compatibles,   condicionados   e incompatibles.
