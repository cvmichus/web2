
<p>
¿Cómo es en México? <br>
Amplio y multidisciplinario – trata diferentes aspectos de la vida en los asentamientos humanos.
</p>
<p>
¿Que abarca? <br>
Se extiende a distintas áreas de la ley (planeación, fiscal, derechos fundamentales).
</p>
<p>
¿Para qué sirve?<br> 
Tiene distintas funciones
Determina condiciones para el acceso al suelo, infraestructura, vivienda, servicios básicos;
Impone obligaciones (impuestos) 
Determina estándares para la planeación y el desarrollo urbano
Define el marco de gobernanza y toma de decisiones
</p>
