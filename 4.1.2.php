¿Por qué es importante? <br>

La legislación urbana es importante pues tiene el potencial de generar marcos efectivos para la sostenibilidad de las ciudades. Siempre y cuando se aplique adecuadamente.
Los tres marcos jurídicos en los tres niveles de gobierno (Federal, Estatal y municipal) se hacen operativos en la planeación urbana, la cual tiene como límites a la actuación de la autoridad el respeto a los derechos fundamentales. 
En ese sentido, la Iniciativa de Ciudades Emergentes y Sostenibles del BID puede aportar al cumplimiento del marco jurídico para cumplir con los objetivos de desarrollo para los que las leyes fueron creadas. 

